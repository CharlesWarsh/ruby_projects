class Person

	def setup
		@age = 0
		@occupation = nil
		@mood = nil
	end

	def set_age(age)
		@age = age
	end

	def set_occupation(occupation)
		@occupation = occupation
	end

	def set_mood(mood)
		@mood = mood
	end

	def age
		@age
	end

	def occupation
		@occupation
	end

	def mood
		@mood
	end

end